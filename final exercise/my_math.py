def add(a=0, b=0):
    """ calculate the sum of a and b.
        :param a: first argument (default = 0)
        :type a: int
        :param b: second argument (default = 0)
        :type b: int
        :rtype: int
    """
    return a + b


def main():
    # print black space and call the function add
    print('\n')
    print(add(2, 2))


if __name__ == '__main__':
    main()
